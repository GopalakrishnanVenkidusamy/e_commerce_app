import 'package:flutter/material.dart';

/// A utility class that provides predefined [TextStyle] constants
/// for consistent text formatting throughout the application.
///
/// This class includes various text styles with different font sizes
/// and weights for use in widgets.
class ECTextStyles {

  // ------------------- Text Style Constants ------------------- //

  /// A [TextStyle] with a font size of 10 pixels.
  static const TextStyle textStyleFontSize10 = TextStyle(fontSize: 10);

  /// A [TextStyle] with a font size of 12 pixels.
  static const TextStyle textStyleFontSize12 = TextStyle(fontSize: 12);

  /// A [TextStyle] with a font size of 14 pixels.
  static const TextStyle textStyleFontSize14 = TextStyle(fontSize: 14);

  /// A [TextStyle] with a font size of 16 pixels.
  static const TextStyle textStyleFontSize16 = TextStyle(fontSize: 16);

  /// A [TextStyle] with a font size of 18 pixels.
  static const TextStyle textStyleFontSize18 = TextStyle(fontSize: 18);

  /// A [TextStyle] with a font size of 20 pixels and bold weight.
  static const TextStyle textStyleFontSize20Bold = TextStyle(
    fontSize: 20,
    fontWeight: FontWeight.bold,
  );
}
